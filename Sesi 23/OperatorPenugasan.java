public class OperatorPenugasan {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        b += a;
        System.out.println("tambah = " + b);

        b -= a;
        System.out.println("kurang = " + b);

        b *= a;
        System.out.println("kali = " + b);

        b /= a;
        System.out.println("bagi = " + b);

        b %= a;
        System.out.println("mod = " + b);

    }
}
